#!/bin/bash

month="December"

if [ $month == "Jan" ]
then echo 1
elif [ $month == "Feb" ]
then echo 2
elif [ $month == "Mar" ]
then echo 3
elif [ $month == "Apr" ]
then echo 4
elif [ $month == "May" ]
then echo 5
elif [ $month == "Jun" ]
then echo 6
elif [ $month == "Jul" ]
then echo 7
elif [ $month == "Aug" ]
then echo 8
elif [ $month == "Sep" ]
then echo 9
elif [ $month == "Oct" ]
then echo 10
elif [ $month == "Nov" ]
then echo 11
elif [ $month == "Dec" ] || [ $month == "DeCemBer" ]
then echo 12
fi